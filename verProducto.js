const firstButton = document.getElementById("firstButton")

firstButton.addEventListener("click", ()=> {
    const div = document.getElementById("div-class");
    const x = document.getElementById("universal-x")
    if (div.classList.contains("container-ver-prod-hidden")) {
        div.classList.remove("container-ver-prod-hidden")
        div.classList.add("container-ver-prod")
    }

    x.addEventListener("click", ()=> {
        if (div.classList.contains("container-ver-prod")) {
            div.classList.remove("container-ver-prod")
            div.classList.add("container-ver-prod-hidden")
        }
    })
})

